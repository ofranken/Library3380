SELECT Title FROM BOOK, BOOK_GENRE WHERE BOOK.ISBN = BOOK_GENRE.ISBN AND Genre = "Fantasy";

SELECT DISTINCT B.Title, BA.Author, F.Quantity, L.Name FROM BOOK AS B INNER JOIN BOOK_AUTHOR AS BA ON B.ISBN = BA.ISBN 
INNER JOIN FORMAT AS F ON B.ISBN = F.ISBN INNER JOIN LOCATION AS L ON F.Location_ID = L.Location_ID 
WHERE F.Format_type = 'Hardcopy' AND F.Quantity > 8;

SELECT BOOK.Title FROM BOOK, FORMAT WHERE Format_type = 'Hardcopy';